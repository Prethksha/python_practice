# 1. List (ordered, changeable, allows duplicates)
my_list = [1, 2, 3, "apple", "banana"]

my_tuple = (1, 2, 3, "apple", "banana")

# 3. Set 
my_set = {1, 2, 3, "apple", "banana"}


# 4. Dictionary 
my_dictionary = {
    "key1": 1,
    "key2": "value",
    "key3": True,
    "list_key": [1, 2, 3]
}

# Printing the results to show they were created:
print(f"List: {my_list}")
print(f"Tuple: {my_tuple}")
print(f"Set: {my_set}")
print(f"Dictionary: {my_dictionary}")
